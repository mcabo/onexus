package org.onexus.core.resources;

import java.io.Serializable;

public class Parameter implements Serializable {

    private String key;
    private String title;
    private String description;
    private boolean mandatory;
    private Class<?> valueType;
    private Object defaultValue;

    public Parameter() {
        super();
    }

    public Parameter(String key, String title, String description,
                     boolean mandatory, Object defaultValue, Class<?> valueType) {
        super();
        this.key = key;
        this.title = title;
        this.description = description;
        this.mandatory = mandatory;
        this.defaultValue = defaultValue;
        this.valueType = valueType;
    }

    public Class<?> getValueType() {
        return valueType;
    }

    public boolean isMandatory() {
        return mandatory;
    }

    public void setMandatory(boolean mandatory) {
        this.mandatory = mandatory;
    }

    public String getKey() {
        return key;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Object getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(Object defaultValue) {
        this.defaultValue = defaultValue;
    }

    @Override
    public String toString() {
        return "Parameter [key=" + key + ", title=" + title + ", description="
                + description + ", mandatory=" + mandatory + ", valueType="
                + valueType + ", defaultValue=" + defaultValue + "]";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((key == null) ? 0 : key.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Parameter other = (Parameter) obj;
        if (key == null) {
            if (other.key != null)
                return false;
        } else if (!key.equals(other.key))
            return false;
        return true;
    }

}
